//Node js log module
// Simple log
console.log('Hello world');
//Log with format specifier
console.log('Hello %s', 'World');   
//Warn log : The console.warn() function is used to display warning message on console.
const name = 'John';  
console.warn(`Don't mess with me ${name}! Don't mess with me!`);
// Error log : The console.error() function is used to render error message on console.
console.error(new Error('Hell! This is a wrong method.'));  